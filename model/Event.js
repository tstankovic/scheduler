const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const eventSchema = new Schema({
  description: {
    type: String,
    required: true,
  },
  day: {
    type: Number,
    required: true,
  },
  month: {
    type: Number,
    required: true,
  },
  year: {
    type: Number,
    required: true,
  },
  user: {
    type: Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
});

const model = mongoose.model('Event', eventSchema);

module.exports = model;
